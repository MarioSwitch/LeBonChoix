<h1>À propos du site</h1>
<h2>Description</h2>
<p>
<i>Le Bon Choix</i> est un jeu où un certain nombre de joueurs doivent choisir un nombre entre 1 et une limite fixée.<br>
Une fois que le nombre de joueurs requis est atteint, ceux ayant été les seuls à choisir leur nombre remportent le jeu.<br>
Les joueurs ayant choisi des nombres pris par plusieurs personnes perdent.<br>
</p>
